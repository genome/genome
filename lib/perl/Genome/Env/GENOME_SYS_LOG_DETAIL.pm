package Genome::Env::GENOME_SYS_LOG_DETAIL;

=pod

=head1 NAME

GENOME_SYS_LOG_DETAIL

=head1 DESCRIPTION

The GENOME_SYS_LOG_DETAIL environment, when set to 1, will cause more detailed logs in JSON format 
to be emitted.  Those logs can be indexed by eleastic search or other aggregation systems.

=head1 DEFAULT VALUE

No value is set by default.

=cut

1;
